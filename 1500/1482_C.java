    public static void solve() {
      int n = sc.nextInt();
      int m = sc.nextInt();
      List<List<Integer>> ll = new ArrayList<>();
      for(int i = 0; i < m; i++){
        ll.add(new ArrayList<>());
        int k = sc.nextInt();
        for(int j = 0; j < k; j++){
          ll.get(i).add(sc.nextInt());
        }
      }

      // 1. Try brute force
      HashMap<Integer,Integer> freq = new HashMap<>();
      int[] days = new int[m+1];
      for(int i = 0; i < ll.size(); i++){
        List<Integer> l = ll.get(0);

        boolean flag = true;
        for(int j = 0; j < l.size(); j++){
          int v = l.get(j);
          freq.put(v, freq.getOrDefault(v, 0) + 1);
          
          if(freq.get(v)/2 > n){
            // we need to try other values
            freq.put(v, freq.get(v) - 1);
            continue;
          }else{
            // we got a value
            days[i] = v;
            flag = false;
            break;
          }
        }

        // if flag is still false, means every is > m/2, so now we need to remvoe the some values
      }
    }

    public static long gcd(long a, long b) {
        while (b != 0) {
            long rem = a % b;
            a = b;
            b = rem;
        }
        return a;
    }

